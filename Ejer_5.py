#Limpiador de texto

Palabra = input("Digite una palabra: ")

MAYUSCULA = Palabra.upper()
minuscula = Palabra.lower()
Capitaliza_Palabras = Palabra.title()
Mayuscula_solo_primera = Palabra.capitalize()

print (f"MAYUSCULA: {MAYUSCULA}")
print (f"minuscula: {minuscula}")
print (f"Mayuscula_Cada_Palabra: {Capitaliza_Palabras}")
print (f"Mayuscula_solo_primera_palabra: {Mayuscula_solo_primera}")