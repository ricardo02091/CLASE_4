# Simple calculator

print("======= Calculadora =======")
numero1 = float(input("Digite el primer número: "))
numero2 = float(input("Digite el segundo número: "))

print(f"suma: {numero1 + numero2}")
print(f"resta: {numero1 - numero2}")
print(f"multiplicacion: {numero1 * numero2}")
print(f"division: {numero1 / numero2}")
print(f"division entera: {numero1 // numero2}")
print(f"residuo: {numero1 % numero2}")
print(f"potencia: {numero1 ** numero2}")


